	function loadcom(q) {
		$('#keyword1,#keyword2').val(q);
		q = q.replaceAll("'", "");
		q = encodeURI(q);
		// 输入结束才发送请求
        if (ajaxTimer) {
        	window.clearTimeout(ajaxTimer);
        }
		ajaxTimer = window.setTimeout(function() {// 只有input有值才发送请求
			q !== '' && 
			$.ajax({
				url : "http://search.5184.com/suggest?q=" + q /* 输入你的serverUrl */,
				xhrFields : {
					withCredentials : true
				},
				dataType : "jsonp",
				jsonp : "callbackparam",// 服务端用于接收callback调用的function名的参数
				jsonpCallback : "success_jsonpCallback",// callback的function名称
				success : function(data) {
                        //console.log(data)
                 }
			});
		}, delay);
	};
   
   
