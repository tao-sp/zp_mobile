	$('#keyword1,#keyword2').on('input', function() {
    	if($(this).prop('comStart')) return;	// 中文输入过程中不截断,输入实时触发
    	var value = $(this).val();
    	loadcom(value);
    }).on('compositionstart', function(){
    	$(this).prop('comStart', true);
    	console.log('中文输入：开始');
    }).on('compositionend', function(){
		$(this).prop('comStart', false);
		loadcom($(this).val());
		console.log('中文输入：结束');
    });

    var ajaxTimer = null;
